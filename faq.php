<script>
$(document).ready(function () {
    $("#panels > div").hide();

    $("#panels h2").on("click", function () {
        const icon = $(this).find("img");
        const panel = $(this).next("div");

        $("#panels h2").not(this).removeClass("active");
        $("#panels > div").not(panel).stop(true, true).slideUp(300);
        $("#panels h2 img").not(icon).attr("src", "img/plus2.png");

        if (panel.is(":visible")) {
            panel.stop(true, true).slideUp(300);
            icon.attr("src", "img/plus2.png");
            $(this).removeClass("active");
        } else {
            panel.stop(true, true).slideDown(300);
            icon.attr("src", "img/minus.png");
            $(this).addClass("active");
        }
    });
});
</script>

